package Conditionals;

public class ifelseifelse {
  public static void main(String[] args) {
    int marks = 100;

    if (marks > 90) {
      System.out.println("A grade");
    } else if (marks >= 70) {
      System.out.println("B grade");
    } else {
      System.out.println("C grade");
    }

  }

}
